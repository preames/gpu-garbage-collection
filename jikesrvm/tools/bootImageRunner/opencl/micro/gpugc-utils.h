char *strdup(const char *s1) {
  int len = strlen(s1) + 1;
  char* new_str = (char*)malloc(sizeof(char)*len);
  memcpy(new_str, s1, sizeof(char)*len);
  new_str[len-1] = 0;
  return new_str;
}

